package Examen1;

public class Calculadora {

	public Double suma(final Double num1, final Double num2) {
		return num1 + num2;
	}

	public Double resta(final Double num1, final Double num2) {
		return num1 - num2;
	}

	public Double multiplicacion(final Double num1, Double num2) {
		return num1 * num2;
	}

	public Double division(final Double num1, final Double num2) {
		return num1 / num2;

	}
}
