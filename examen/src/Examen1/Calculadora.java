package Examen1;

public class Calculadora {

    public static final String suma = null;
    public static final String resta = null;
    public static final String multipliacion = null;
    public static final String division = null;

    public Double suma(final Double num1, final Double num2) {
        return num1 + num2;
    }

    public Double resta(final Double num1, final Double num2) {
        return num1 - num2;
    }

    public Double multiplicacion(final Double num1, final Double num2) {
        return num1 * num2;
    }

    public Double division(final Double num1, final Double num2) {
        return num1 / num2;

    }
}
